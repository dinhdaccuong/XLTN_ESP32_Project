void led_init()
{
	pinMode(LED_PIN, OUTPUT);
	digitalWrite(LED_PIN, HIGH);	// turn off the led
}
void led_turnOn()
{
	digitalWrite(LED_PIN, LOW);
}
void led_turnOff()
{
	digitalWrite(LED_PIN, HIGH);
}


